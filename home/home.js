function mensagem(){
    removeEventListener('mouseenter', mensagem)
    var fig = document.getElementById('fig')
    var msg = document.createElement('div')
    msg.style.opacity = 0
    msg.setAttribute("id", "msg")
    var p = document.createElement('p')
    var txt = document.createTextNode("Frete grátis e encomenda sob medida")
    setTimeout(function(){
        p.appendChild(txt)
        msg.appendChild(p)
        fig.appendChild(msg)
    }, 1000);

   setTimeout(function(){
    msg.style.opacity = 1
   }, 2000);

     setTimeout(function() {
        msg.style.opacity = 0
    }, 7000);

    setTimeout(function() {
        msg.remove()
    }, 9000);
}



